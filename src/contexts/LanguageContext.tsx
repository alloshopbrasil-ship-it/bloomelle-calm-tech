const languageNames: Record<Language, string> = {
  pt: "Português",
  en: "English",
  es: "Español",
  fr: "Français",
  de: "Deutsch"
};

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

// Map browser language codes to supported languages with English fallback
const getLanguageFromBrowser = (): Language => {
  const browserLang = navigator.language.toLowerCase();
  
  // Check for exact match first
  if (browserLang.startsWith("pt")) return "pt";
  if (browserLang.startsWith("en")) return "en";
  if (browserLang.startsWith("es")) return "es";
  if (browserLang.startsWith("fr")) return "fr";
  if (browserLang.startsWith("de")) return "de";
  
  // Fallback to English for unsupported languages
  return "en";
};

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem("bloomelle-language");
    if (saved && ["pt", "en", "es", "fr", "de"].includes(saved)) {
      return saved as Language;
    }
    // Auto-detect from browser with English fallback
    return getLanguageFromBrowser();
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("bloomelle-language", lang);
  };

  const t = (key: string): string => {
    return translations[language][key] || translations["en"][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, translations: translations[language] }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};

export const getLanguageName = (lang: Language) => languageNames[lang];
